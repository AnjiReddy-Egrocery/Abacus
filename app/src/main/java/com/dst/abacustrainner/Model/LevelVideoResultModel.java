package com.dst.abacustrainner.Model;

public class LevelVideoResultModel {
    private String date, time;

    public LevelVideoResultModel(String date, String time) {
        this.date = date;
        this.time = time;
    }

    public String getDate() {
        return date;
    }

    public String getTime() {
        return time;
    }
}
